// Utilize if/else para fazer um programa que, dado um valor recebido como parâmetro, retorne “positive” se esse valor for positivo, \
//“negative” se for negativo, e caso não seja nem positivo e nem negativo retorne “zero”.
let numero = 0;

if (numero > 0){
    console.log("Positive");
} else if (numero < 0){
    console.log("Negative");
} else {
    console.log("Zero")
}